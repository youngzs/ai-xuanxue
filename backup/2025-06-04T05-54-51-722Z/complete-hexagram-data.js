// 获取卦象的网络图片URL
export function getHexagramImageUrl(number, type = 'main') {
    const hexInfo = hexagramImages.hexagrams[number];
    if (!hexInfo || !hexInfo[type]) {
        return null;
    }
    return hexagramImages.basePath + hexInfo[type];
}

